func RemoveDuplicates(input []string) []string {
	seen := make(map[string]struct{})
	result := make([]string, 0)

	for _, item := range input {
		if _, exists := seen[item]; !exists {
			seen[item] = struct{}{}
			result = append(result, item)
		}
	}
	return result
}

